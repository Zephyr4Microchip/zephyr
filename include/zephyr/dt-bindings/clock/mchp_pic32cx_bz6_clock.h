/**
 * Encode clock type, instance number, register offset and bit position
 * to clock subsystem.
 *
 * - 00..07 (8 bits): bit position
 * (values from 0 to 31)
 *
 * - 08..19 (12 bits): register offset
 *
 * - 20..25 (6 bits): inst
 *
 * - 26..31 (6 bits): type
 * following values
 * SUBSYS_TYPE_POSC       (0)
 * SUBSYS_TYPE_SOSC       (1)
 * SUBSYS_TYPE_SYSCLK     (2)
 * SUBSYS_TYPE_SPLL1      (3)
 * SUBSYS_TYPE_SPLL2      (4)
 * SUBSYS_TYPE_PBCLK      (5)
 * SUBSYS_TYPE_REFCLK     (6)
 * SUBSYS_TYPE_GCLKPERIPH (7)
 * SUBSYS_TYPE_WDTCLK     (8)
 * SUBSYS_TYPE_VBKPCLK    (9)
 * SUBSYS_TYPE_DSWDTCLK   (10)
 * SUBSYS_TYPE_LPCLK      (11)
 * SUBSYS_TYPE_RTCCLK     (12)
 * SUBSYS_TYPE_FRC        (13)
 * SUBSYS_TYPE_LPRC       (14)
 * SUBSYS_TYPE_SPLL3      (15)
 *
 * @param type clock subsystem type
 * @param inst instance number of the specified clock type
 */
#define MCHP_CLOCK_DERIVE_ID(type, inst, offset, bitpos)                                           \
	(((type) << 26) | ((inst) << 20) | ((offset) << 8) | (bitpos))

